#include <stdlib.h>

int		ft_ultimate_range(int **range, int min, int max)
{
	int i;
	int *tab;
	int size;

	size = max - min;
	if (min >= max)
	{
		tab = NULL;
		return (0);
	}
	if (!(tab = (int*)malloc(sizeof(int) * size)))
		return (-1);
	i = 0;
	while (min < max)
	{
		tab[i] = min;
		min++;
		i++;
	}
	*range = tab;
	return (size);
}
